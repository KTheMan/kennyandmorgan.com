create extension if not exists pgcrypto;

create table if not exists public.access_passwords (
    level text primary key check (level in ('family', 'party', 'admin')),
    password_hash text not null,
    created_at timestamptz not null default timezone('utc', now()),
    updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.access_sessions (
    token_hash text primary key,
    access_level text not null check (access_level in ('family', 'party', 'admin')),
    expires_at timestamptz not null,
    created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.guests (
    id bigint generated by default as identity primary key,
    full_name text not null,
    email text,
    group_id text not null,
    is_primary boolean not null default false,
    is_plus_one boolean not null default false,
    notes text,
    rsvp_status text not null default 'pending' check (rsvp_status in ('pending', 'accepted', 'declined')),
    meal_choice text,
    dietary_notes text,
    address_line1 text,
    address_line2 text,
    city text,
    state text,
    postal_code text,
    last_rsvp_at timestamptz,
    created_at timestamptz not null default timezone('utc', now()),
    updated_at timestamptz not null default timezone('utc', now())
);

create index if not exists guests_group_id_idx on public.guests (group_id);
create index if not exists guests_full_name_idx on public.guests using gin (to_tsvector('simple', coalesce(full_name, '')));

create table if not exists public.rsvp_submissions (
    id bigint generated by default as identity primary key,
    group_id text,
    submitter_name text not null,
    submitter_email text,
    attending boolean not null,
    guest_count integer not null default 1,
    meal_choice text,
    dietary_notes text,
    special_message text,
    song_request text,
    created_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.address_submissions (
    id bigint generated by default as identity primary key,
    full_name text not null,
    email text,
    street text,
    street2 text,
    city text,
    state text,
    zip text,
    submitted_at timestamptz not null default timezone('utc', now()),
    raw_payload jsonb not null default '{}'::jsonb
);

