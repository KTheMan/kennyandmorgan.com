create or replace function public.access_rank(level text)
returns integer
language sql
immutable
as $$
    select case lower(coalesce(level, ''))
        when 'family' then 1
        when 'party' then 2
        when 'admin' then 3
        else 0
    end;
$$;

create or replace function public.require_session(session_token text, required_level text default 'family')
returns text
language plpgsql
security definer
set search_path = public
as $$
declare
    session_level text;
begin
    delete from public.access_sessions where expires_at <= timezone('utc', now());

    select access_level
    into session_level
    from public.access_sessions
    where token_hash = encode(digest(coalesce(session_token, ''), 'sha256'), 'hex')
      and expires_at > timezone('utc', now())
    limit 1;

    if session_level is null then
        raise exception 'Unauthorized';
    end if;

    if public.access_rank(session_level) < public.access_rank(required_level) then
        raise exception 'Forbidden';
    end if;

    return session_level;
end;
$$;

create or replace function public.login_access(candidate_password text, session_ttl_ms bigint default 3600000)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    matched_level text;
    raw_token text;
begin
    select level
    into matched_level
    from public.access_passwords
    where crypt(coalesce(candidate_password, ''), password_hash) = password_hash
    order by public.access_rank(level) desc
    limit 1;

    if matched_level is null then
        raise exception 'Invalid password.';
    end if;

    raw_token := encode(gen_random_bytes(32), 'hex');

    insert into public.access_sessions (token_hash, access_level, expires_at)
    values (
        encode(digest(raw_token, 'sha256'), 'hex'),
        matched_level,
        timezone('utc', now()) + make_interval(secs => greatest(session_ttl_ms, 60000) / 1000)
    );

    return jsonb_build_object(
        'success', true,
        'token', raw_token,
        'accessLevel', matched_level,
        'expiresIn', greatest(session_ttl_ms, 60000)
    );
end;
$$;

create or replace function public.get_access_session(session_token text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    session_level text;
    session_expiry timestamptz;
    expires_in_ms bigint;
begin
    select access_level, expires_at
    into session_level, session_expiry
    from public.access_sessions
    where token_hash = encode(digest(coalesce(session_token, ''), 'sha256'), 'hex')
      and expires_at > timezone('utc', now())
    limit 1;

    if session_level is null then
        raise exception 'Unauthorized';
    end if;

    expires_in_ms := greatest(0, floor(extract(epoch from (session_expiry - timezone('utc', now()))) * 1000));

    return jsonb_build_object(
        'success', true,
        'accessLevel', session_level,
        'expiresIn', expires_in_ms
    );
end;
$$;

create or replace function public.logout_access(session_token text)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
begin
    delete from public.access_sessions
    where token_hash = encode(digest(coalesce(session_token, ''), 'sha256'), 'hex');

    return jsonb_build_object('success', true);
end;
$$;

create or replace function public.search_guest_groups(search_name text, max_results integer default 5)
returns jsonb
language sql
security definer
set search_path = public
as $$
with tokens as (
    select regexp_split_to_array(trim(coalesce(search_name, '')), '\s+') as parts
), filtered as (
    select g.*
    from public.guests g, tokens t
    where cardinality(t.parts) >= 2
      and lower(g.full_name) like '%' || lower(t.parts[1]) || '%'
      and lower(g.full_name) like '%' || lower(t.parts[cardinality(t.parts)]) || '%'
), grouped as (
    select distinct group_id
    from filtered
    order by group_id
    limit greatest(coalesce(max_results, 5), 1)
)
select coalesce(jsonb_agg(
    jsonb_build_object(
        'groupId', grp.group_id,
        'primaryGuest', coalesce((
            select full_name
            from public.guests
            where group_id = grp.group_id
            order by is_primary desc, full_name asc
            limit 1
        ), ''),
        'guests', coalesce((
            select jsonb_agg(jsonb_build_object(
                'id', guest.id,
                'fullName', guest.full_name,
                'email', guest.email,
                'isPrimary', guest.is_primary,
                'isPlusOne', guest.is_plus_one,
                'notes', guest.notes,
                'rsvpStatus', guest.rsvp_status,
                'mealChoice', guest.meal_choice,
                'dietaryNotes', guest.dietary_notes,
                'addressLine1', guest.address_line1,
                'addressLine2', guest.address_line2,
                'city', guest.city,
                'state', guest.state,
                'postalCode', guest.postal_code
            ) order by guest.is_primary desc, guest.full_name asc)
            from public.guests guest
            where guest.group_id = grp.group_id
        ), '[]'::jsonb)
    )
), '[]'::jsonb)
from grouped grp;
$$;

create or replace function public.submit_rsvp(payload jsonb)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
    submitter_name text := trim(coalesce(payload->>'name', payload->>'rsvpName', ''));
    submitter_email text := trim(coalesce(payload->>'email', payload->>'rsvpEmail', ''));
    guest_group_id text := nullif(trim(coalesce(payload->>'guestGroupId', '')), '');
    normalized_dietary text := nullif(trim(coalesce(payload->>'dietaryRestrictions', '')), '');
    normalized_message text := nullif(trim(coalesce(payload->>'specialMessage', '')), '');
    normalized_song_request text := nullif(trim(coalesce(payload->>'songRequest', '')), '');
    normalized_meal_choice text := nullif(trim(coalesce(payload->>'mealChoice', '')), '');
    guest_responses jsonb := coalesce(payload->'guestResponses', '[]'::jsonb);
    accepted_count integer := 0;
    attending_flag boolean := false;
begin
    if submitter_name = '' then
        raise exception 'RSVP name is required.';
    end if;

    if submitter_email = '' then
        raise exception 'RSVP email is required.';
    end if;

    if guest_group_id is null and jsonb_array_length(guest_responses) > 0 then
        raise exception 'guestGroupId is required when submitting per-guest responses.';
    end if;

    select count(*) filter (where lower(coalesce(entry.value->>'status', '')) = 'accepted')
    into accepted_count
    from jsonb_array_elements(guest_responses) as entry(value);

    attending_flag := accepted_count > 0 or lower(coalesce(payload->>'attending', '')) in ('yes', 'true');

    insert into public.rsvp_submissions (
        group_id,
        submitter_name,
        submitter_email,
        attending,
        guest_count,
        meal_choice,
        dietary_notes,
        special_message,
        song_request
    ) values (
        guest_group_id,
        submitter_name,
        submitter_email,
        attending_flag,
        greatest(coalesce(nullif(payload->>'guestCount', '')::integer, accepted_count, 1), 1),
        normalized_meal_choice,
        normalized_dietary,
        coalesce(normalized_message, normalized_song_request),
        normalized_song_request
    );

    if jsonb_array_length(guest_responses) > 0 then
        update public.guests guest
        set rsvp_status = case lower(coalesce(entry.value->>'status', ''))
                when 'accepted' then 'accepted'
                when 'declined' then 'declined'
                else guest.rsvp_status
            end,
            meal_choice = coalesce(nullif(trim(coalesce(entry.value->>'mealChoice', '')), ''), guest.meal_choice),
            full_name = coalesce(nullif(trim(coalesce(entry.value->>'name', '')), ''), guest.full_name),
            last_rsvp_at = timezone('utc', now()),
            updated_at = timezone('utc', now())
        from jsonb_array_elements(guest_responses) as entry(value)
        where guest.id = nullif(entry.value->>'guestId', '')::bigint
          and guest.group_id = guest_group_id;
    elsif guest_group_id is not null then
        update public.guests
        set rsvp_status = case when attending_flag then 'accepted' else 'declined' end,
            meal_choice = coalesce(normalized_meal_choice, meal_choice),
            last_rsvp_at = timezone('utc', now()),
            updated_at = timezone('utc', now())
        where group_id = guest_group_id;
    end if;

    if guest_group_id is not null and normalized_dietary is not null then
        update public.guests
        set dietary_notes = normalized_dietary,
            updated_at = timezone('utc', now())
        where group_id = guest_group_id;
    end if;

    return jsonb_build_object(
        'success', true,
        'status', case when attending_flag then 'accepted' else 'declined' end,
        'message', case when attending_flag
            then 'Thank you for your RSVP! We cannot wait to celebrate with you.'
            else 'We appreciate the update and will miss you at the celebration.'
        end
    );
end;
$$;

